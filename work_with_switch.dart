List<String> fruits = ['apple', 'banana', 'cherry', 'date'];
int index = 2;

switch (fruits[index]) {
  case 'apple':
    print('You chose an apple');
    break;
  case 'banana':
    print('You chose a banana');
    break;
  case 'cherry':
    print('You chose a cherry');
    break;
  case 'date':
    print('You chose a date');
    break;
  default:
    print('Invalid fruit');
}
